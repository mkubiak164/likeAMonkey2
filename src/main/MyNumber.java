package main;

public class MyNumber {

    int liczba;

    public MyNumber (int liczba) {
        this.liczba = liczba;
    }

    public boolean isOdd(int liczba) {
        if(liczba%2 == 1) {
            return true;
        } else {
            return false;
        }
    }

    public boolean isEven(int liczba) {
        if(liczba%2 == 0) {
            return true;
        } else {
            return false;
        }
    }

    public double sqrt(int liczba) {
        return sqrt(liczba);
    }

    public double pow(double liczba, double b) {
        return pow(liczba, b);
    }








}
